                <p style="font-size:20px"><b>1. Introduction</b></p>
                <br><br>
                <p style="text-align:justify;">The Nadia single cell instrument is a microfluidic solution that automatically runs 1, 2, 4 or 8 cells samples in parallel in under 20 minutes. By automatically encapsulating single cells in microfluidic droplets with mRNA capture beads. Over 50,000 single cells are captured per cartridge in a run. After sequencing, this enables detailed profiling of large heterogeneous cell populations.</p>
                <br><br><br>
                <p style="font-size:20px"><b>2. Features</b></p>
                <br><br>
                <ul>
                    <li>High throughput – 5,000 – 10,000 single cell transcriptomes per run</li>
                    <li>High capture rate of around 11% of transcriptomes of cells</li>
                    <li>Low doublet rate of about 5%</li>
                    <li>Simplicity</li>
                    <li>Commercial availability of the barcoded beads</li>
                    <li>Relatively low cost</li>
                </ul>
                <br><br>
                <p style="font-size:20px"><b>3. Core Principles of Drop-seq</b></p>
                <br><br>
                <ul>
                    <li>Encapsulation of single cells with DNA-barcoded microbeads</li>
                    <li>The use of a microfluidic device/chip to create nanoliter-scale aqueous droplets formed by precisely combining aqueous and oil flows</li>
                    <li>The use of molecular barcoding beads to distinguish the cell-of-origin of each mRNA</li>
                    <li>Reverse transcription added post encapsulation</li>
                </ul>
                <br><br>
                <p style="font-size:20px"><b>4. Methodology</b></p>
                <br><br>
                <p style="text-align:justify;">The methodology involves encapsulating single cells with single barcoded beads in nanoliter-sized droplets. The barcoded oligo bead library is constructed such that each bead has a unique DNA barcode sequence, but within a bead, the thousands of copies of oligo all contain an identical barcode sequence. The 3′ end of the oligo has a poly(dT) stretch that captures mRNA and primes reverse transcription. Once encapsulated, the cell is broken open and the mRNA is captured on the bead, resulting in STAMPs (Single-cell Transcriptomes Attached to MicroParticles). The RNA is converted to DNA, amplified and sequenced.</p>
