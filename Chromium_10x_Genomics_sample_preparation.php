            <br><br>
            <p style="font-size:20px"><b>4. Principle of Chromium Single Cell Solution</b></p>
            <br><br>
            <p style="text-align:justify;">10x Genomics’ single-cell RNA-seq (scRNA-seq) technology, the Chromium&trade; Single Cell 3’ Solution, allows you to analyze transcriptomes on a cell-by-cell basis through the use of microfluidic partitioning to capture single cells and prepare barcoded, next-generation sequencing (NGS) cDNA libraries. Specifically, single cells, reverse transcription (RT) reagents, Gel Beads containing barcoded oligonucleotides, and oil are combined on a microfluidic chip to form reaction vesicles called Gel Beads in Emulsion, or GEMs. GEMs are formed in parallel within the microfluidic channels of the chip, allowing the user to process 500 to 10000 of single cells per channel in a single 7-minute Chromium&trade; Instrument run. It’s important to note that cells are loaded at a limiting dilution in order to maximize the number of GEMs containing a single cell to ensure a low doublet rate, while maintaining a high cell recovery rate of up to ~65%.</p>
            <br><br>
            <p style="text-align:justify;">Each functional GEM contains a single cell, a single Gel Bead, and RT reagents. Within each GEM reaction vesicle, a single cell is lysed, the Gel Bead is dissolved to free the identically barcoded RT oligonucleotides into solution, and reverse transcription of polyadenylated mRNA occurs. As a result, all cDNAs from a single cell will have the same barcode, allowing the sequencing reads to be mapped back to their original single cells of origin. The preparation of NGS libraries from these barcoded cDNAs is then carried out in a highly efficient bulk reaction.</p>
