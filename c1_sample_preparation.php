	<td>
	<p>Fluidigm C1&trade; system allows users to rapidly capture and isolate up to 96 individual cells in one reaction by processing with C1&trade; Integrated Fluidic Circuits (IFCs) based on three cell sizes – small cells (5–10 &micro;m), medium cells (10–17 &micro;m), and large cells (17–25 &micro;m). This service also provides accurate and sensitive whole-transcriptome profiling of single cells, in addition to viewing multiple cell types in one chip when different CellTracker dyes are used.<br><br>
	Each C1 run will take approximately 2 to 3 days including: capturing cells, staining for viability, imaging cells, lysing cells, performing reverse transcription, long-distance PCR amplification, harvesting the amplified cDNA and generation of sequencing-ready cDNA libraries.<br>A summary of this experimental scheme is shown below.</p>
	<br><br>
	<center><img src="layout/c1pipeline.jpg"></center>
	<br>
	<p>Referring to Figure 1, cDNA synthesis is performed on C1 by using the Clontech SMARTer&reg; Ultra&reg; Low RNA Kit (Cat# 634936). Target mRNA (with PolyA+ tail) from captured cells is reverse transcribed into full-length cDNA for universal amplification. Harvest cDNA amplicons can then be further quantified on the Agilent&reg; Bioanalyzer. If desired, library sequencing can be done by the Illumina HiSeq system.</p>
	<br><br>
	<center><img src="layout/c1chemistry.jpg" style="width:750"></center>
