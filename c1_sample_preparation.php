	<h6>SAMPLE PREPARATION<h6><br>
	<h6>A. PREPARED BY USERS<h6>
	<br>
	<p>Prior to starting the experiment, users should harvest, wash, and count cells.
	<br><br>
	<ul>
	<li>Cells need to be freshly dissociated into single-cell suspension for C1 run, please kindly notify a responsible person at least three days before the time of cell harvest</li>
	<li>It is also important to check cells under a microscope after cell counting to ensure cells are dispersed</li>
	<li>He/she should fill a submission form about the number of runs (max. of two cell lines/run) to be performed and other respective information</li>
	<li>The service will only be provided after all the details have been acknowledged by the core</li>
	</ul>
	<br>
	<p>All samples should meet the following criteria for submission:<br><br>
	1. Cells are required to be dissociated into single cell suspension<br>
	2. Please check out from the core website, the appropriate C1 IFC regarding to your sample cell size<br>
	3. If you are wish to perform two cell lines per chip, please make sure the two cell sizes are within the same size range<br>
	4. Submit at least 500 &micro;L of 3.0 x 10<sup>5</sup> cells/mL for each sample*</p><br><br><br>
	
