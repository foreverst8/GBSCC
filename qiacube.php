	<h6>A. DESCRIPTION AND APPLICATIONS<h6>
	<br>
	<p>The QIAcube performs fully automated processing of up to 12 samples each time. The instrument controls integrated components including a centrifuge, heated shaker, pipetting system, and robotic gripper.
	<br><br>
	The QIAcube is preinstalled with various protocols for processing QIAGEN spin columns for purification of RNA, genomic DNA, plasmid DNA, viral nucleic acids, or proteins, plus DNA and RNA cleanup.
	<br><br>
	The user selects a protocol using the touchscreen, and loads plasticware, samples, and reagents onto the QIAcube worktable. The user then closes the instrument door and starts the protocol, which provides all necessary commands for sample lysis and purification using QIAGEN spin columns. A fully automated load check helps to ensure correct loading of the worktable.
	<br><br>
