	<h6>A. INTRODUCTION<h6>
	<br>
	<p>Pulsed field electrophoresis is a powerful technique for resolving chromosomal sized DNAs. Alternating the electric field between spatially distinct pairs of electrodes causes megabase (mb) size DNAs to re-orient and move at different speeds through the pores in an agarose gel. The CHEF Mapper system separates large and small DNA fragments with better resolution, speed, and accuracy than traditional pulsed field methods. DNAs ranging from 100 bases to over 10 megabases may be effectively resolved.
	<br><br>
	The CHEF Mapper system is based on two leading technologies, CHEF (Clamped Homogeneous Electric Fields) and PACE (Programmed Autonomously Controlled Electrodes). The system provides highly uniform, or homogeneous, electric fields within the gel, using an array of 24 electrodes, some of which are clamped, or held to intermediate potentials to eliminate lane distortion.</p>
	<br><br>
	<h6>B. APPLICATIONS<h6>
	<br>
	<p>The CHEF Mapper XA system lets you choose any pulse angle from 0 to 360Â°, which allows optimal separation of both chromosomal and plasmid DNA with one system. Accurate sizing of fragments requires an expanded linear range of separation. Switch-time ramps increase the mobility of fragments by gradually changing the switch times during the course of a run. Nonlinear (for example, concave or convex) ramps change the switch-time increments from the start to the end of a run. These nonlinear ramps separate fragments linearly from 50 to 700 kb, yielding more precise fragment size measurements.
	<br><br>
	The multistate mode of the CHEF Mapper XA system enhances resolution in selected fragment size ranges. Each vector (angle of pulse) can be assigned its own voltage (field intensity) and its own switch time (duration of pulse). Up to eight different states can be combined into one run to optimize the separation of subsets of fragments in the sample.</p>
