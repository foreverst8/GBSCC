	<h6>A. INTRODUCTION<h6>
	<br>
	<p>This technology couples the high-throughput nature of flow cytometry with the high specificity of time-of-flight mass spectrometry. It is widely popular method for studying protein expression at single-cell resolution.</p>
	<br><br>
	<h6>B. CURRENT APPLICATIONS<h6>
	<br>
	<p>Currently, significant efforts are being devoted to immunophenotyping in tumour immunology, leading to major discoveries such as identification of rare cell populations in the T-cell and macrophage components. In addition, the technology has also been adapted to quantify intracellular signaling activity, due to the increased availability of channels (up to 80) compared to conventional flow cytometry.</p>
