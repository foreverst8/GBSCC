	<h6>C. ANTIBODIES<h6>
	<br>
	<p>Metal conjugated antibodies can be purchased from Fluidigm. These antibodies are tested and titred by their in-house technicians and the products are ready to use. (Refer link under Maxpar Metal-Conjugated Antibodies: <a class="button" target="_blank" href="https://www.fluidigm.com/reagents/mass-cytometry#products">Click</a>)<br><br>
	Alternatively, users may opt to manually conjugate their own antibodies given they are compatible with flow cytometry. The core personnels can assist users on this approach (Protocol adapted from Fluidigm Maxpar Antibody Labeling Kit: <a class="button" target="_blank" href="https://www.fluidigm.com/binaries/content/documents/fluidigm/resources/maxpar-antibody-labeling-kit-pr-prd002/maxpar-antibody-labeling-kit-pr-prd002/fluidigm%3Afile">Click</a>)
	<br><br>
	Antibodies that are used for conjugation need to be carrier-free, i.e. free from the common additive BSA. If antibodies contain BSA, it can be removed by purification using kits available from Abcam (#102784). Biolegend have antibodies that are tailored for self-conjugation and they are labelled as ‘MaxPar Ready’. (For more information please refer to their website: <a class="button" target="_blank" href="http://www.biolegend.com/NewsLegend/010314/index.htm">Click</a>)</p>
	<br><br>
	<h6>D. EXPERIMENTAL DESIGN<h6>
	<br>
	<p>Once users have determined the type of antibodies (i.e. metal-conjugated antibodies or they want to manually label their own antibody with any metal) to tag their cells, they can decide the method of cell staining based on two major protocols:</p>
	<br><br>
	<ul>
	<li>Cell Surface Staining Protocol is intended for staining cell surface markers. (Protocol adapted from Maxpar&reg; Cell Surface Staining: <a class="button" target="_blank" href="https://www.fluidigm.com/binaries/content/documents/fluidigm/resources/maxpar-cell-surface-staining-prd012/maxpar-cell-surface-staining-prd012/fluidigm%3Afile">Click</a>)</li>
	<li>Nuclear Antigen Staining Protocol is used for staining both cell surface and intracellular targets. (Protocol adapted from Maxpar&reg; Nuclear Antigen Staining: <a class="button" target="_blank" href="https://www.fluidigm.com/binaries/content/documents/fluidigm/resources/maxpar-nuclear-antigen-staining-protocol/maxpar-nuclear-antigen-staining-protocol/fluidigm%3Afile">Click</a>)</li>
	</ul>
	<br><p>If you are interested in reading more details about this technology, kindly refer to the following publications:<br><br>
