	<h6>E. SAMPLE PREPARATION</h6>
	<br>
	<p>Cells need to be dissociated into single cell suspension. For peripheral blood mononuclear cells (PBMCs), it is advised to add benzonase to digest free DNA and avoid cell sticking.<br><br>
	Our protocols are optimized for staining 1 – 3 x 10<sup>6</sup> cells in one reaction.<br><br>
	Users will be trained and assisted by a core personnel during the whole course of the work flow. Prior to starting the experiment, users should harvest, wash, count and pellet the cells (see Figure 1 below). They should fill a submission form about the number of samples to be used and other respective information. The service will only be provided after all the details have been acknowledged by the core. Users should also checkout the usage of Argon gas (under User Checkout Section) AFTER their sample run, usually 2 hours of Argon are consumed per run.</p><br><br>
